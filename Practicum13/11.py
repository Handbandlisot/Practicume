def is_valid_sudoku(board) -> bool:
    seen = set()
    
    for r in range(9):
        for c in range(9):
            val = board[r][c]
            
            if val == '.':
                continue
                
            row_marker = f"row {r} has {val}"
            col_marker = f"col {c} has {val}"
            box_marker = f"box {r // 3} - {c // 3} has {val}"
            
            if row_marker in seen or col_marker in seen or box_marker in seen:
                return False
                
            seen.add(row_marker)
            seen.add(col_marker)
            seen.add(box_marker)
            
    return True


